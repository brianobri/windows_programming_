public class ComplexCalc
{


    public int addreal(int a, int b)
    {
	return a+b;
    }

    public int addim(int a, int b)
    {
	return a+b;
    }


    public int subreal(int a, int b)
    {
	return a-b;
    }

    public int subim(int a, int b)
    {
	return a-b;
    }


    public int multreal(int a, int b)
    {
	return a*b;
    }

    public int multim(int a, int b)
    {
	return a*b;
    }


    public double divreal(double a, double b)
    {
	return a/b;
    }

    public double divim(double a, double b)
    {
	return a/b;
    }
}