public class SimpleCalc
{	

    public int add(int a, int b)
    {	
        return a+b;
    }

    public int sub(int a, int b)
    {
        return a-b;
    }

    public int mult(int a, int b)
    {
        return a*b;
    }

    public double div(double a, double b)
    {
        return a/b;
    }
}